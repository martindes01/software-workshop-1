/**
 * The MovieQuoteInfo class displays a movie quote, the movie it comes from, the
 * character who said it, and the year of the movie.
 *
 * @author Martin
 * @version 1.0.0
 */
public class MovieQuoteInfo {

	public static void main(String[] args) {
		System.out.println("\"Hello there.\"");
		System.out.println("\t-- Obi-Wan Kenobi, Star Wars: Episode III -- Revenge of the Sith (2005)");
	}

}
